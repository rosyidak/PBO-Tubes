    private void jLabel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel8MouseClicked
        // TODO add your handling code here:
        String id= txtidpengajar.getText();
        String siswa = txtidsiswa.getText();
        if(siswa.equals("")||id.equals("")){
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!");
        }else{
            try{
                MysqlDataSource submit = new MysqlDataSource();
                submit.setServerName("localhost");
                submit.setUser("root");
                submit.setPassword("");
                submit.setPortNumber(3306);
                submit.setDatabaseName("private_musik");

                Connection koneksi = submit.getConnection();

                ResultSet rs = koneksi.prepareStatement("select hari,Id_Siswa from jadwal "+
                        "where Id_Siswa='"+txtidsiswa.getText()+"' and hari='"+txthariadmin.getSelectedItem()+"'").executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(null, "Jadwal siswa ada yang bertabrakan!");
                }else{
                    try{                    
                        koneksi.prepareStatement("insert into jadwal values ('"+
                            txtidjadwal.getText()+"','"+
                            txthariadmin.getSelectedItem()+"','"+
                            txtjamadmin.getSelectedItem()+"','"+
                            txtidpengajar.getText()+"','"+
                            txtidsiswa.getText()+"','"+                
                            txtalatmusik.getSelectedIndex()+"','"+
                            txtruangan.getSelectedItem()+"')").execute();

                        autonumber();
                        tampil();
                        txtalatmusik.setSelectedItem("Gitar");
                        txtidpengajar.setText("");
                        txtidsiswa.setText("");
                        txthariadmin.setSelectedItem("Senin");
                        txtjamadmin.setSelectedItem("19:00:00");
                        txtruangan.setSelectedItem("B001");
                        JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                    }catch(Exception e){
                        System.out.println(e);

                    }
                }
            }catch(Exception e){
                System.out.println(e);
            }
        }                  
    }//GEN-LAST:event_jLabel8MouseClicked

    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
        // TODO add your handling code here:
        int jawab = JOptionPane.showConfirmDialog(null, "Apakah anda Ingin Menghapus?");
        
        switch(jawab){
            case JOptionPane.YES_OPTION:
                if(tabeljadwaladmin.getSelectedColumnCount()!=-1){
                    try{
                        MysqlDataSource submit = new MysqlDataSource();
                        submit.setServerName("localhost");
                        submit.setUser("root");
                        submit.setPassword("");
                        submit.setPortNumber(3306);
                        submit.setDatabaseName("private_musik");

                        Connection koneksi=submit.getConnection();

                        koneksi.prepareStatement(
                            "DELETE FROM jadwal WHERE Id_Jadwal ='"+
                            tabeljadwaladmin.getValueAt(tabeljadwaladmin.getSelectedRow(), 0).toString()+
                            "' ").executeUpdate();

                        autonumber();
                        tampil();
                    }catch(Exception e){
                        System.out.println(e);
                    }
                }
                break;
            case JOptionPane.NO_OPTION:
                break;
            case JOptionPane.CANCEL_OPTION:
                break;
        }
    }//GEN-LAST:event_jLabel9MouseClicked

