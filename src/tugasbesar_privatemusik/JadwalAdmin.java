    private void jLabel17MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel17MouseClicked
        // TODO add your handling code here:
        String pengajar=txtidpengajar.getText();
        String siswa=txtidsiswa.getText();
        if(pengajar.equals("")||siswa.equals("")){
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!");
        }else{
            try{
                MysqlDataSource submit = new MysqlDataSource();
                submit.setServerName("localhost");
                submit.setUser("root");
                submit.setPassword("");
                submit.setPortNumber(3306);
                submit.setDatabaseName("private_musik");

                Connection koneksi=submit.getConnection();

                koneksi.prepareStatement(
                    "update jadwal set hari = '"+txthariadmin.getSelectedItem()+
                    "', jam = '"+txtjamadmin.getSelectedItem()+                        
                    "', Id_Pengajar = '"+txtidpengajar.getText()+
                    "', Id_Siswa = '"+txtidsiswa.getText()+                        
                    "', Id_Alat_Musik='"+txtalatmusik.getSelectedIndex()+
                    "' where Id_Jadwal='"+txtidjadwal.getText()+"' ").execute();

                tampil();
                JOptionPane.showMessageDialog(null, "Data berhasil diperbaharui!");
                txthariadmin.setSelectedItem("Senin");
                txtjamadmin.setSelectedItem("19:00:00");
                txtidsiswa.setText("");
                txtalatmusik.setSelectedIndex(0);
                txtidpengajar.setText("");                
            }catch(Exception e){
                System.out.println(e);

            }   
        }
    }//GEN-LAST:event_jLabel17MouseClicked

